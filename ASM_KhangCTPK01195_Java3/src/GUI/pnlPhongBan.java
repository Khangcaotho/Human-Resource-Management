    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int soDongDuocChon = tblPhongBan.getSelectedRowCount();
        if(soDongDuocChon < 1){
            ThongBao.ThongBao("Bạn cần chon 1 dòng để sửa", "Thông báo");
            return;
        }
            String tenPB = txtTenPB.getText();
            String diaChi = txtdc.getText();
            String SDT = txtsdt.getText();
            
            int viTri=tblPhongBan.getSelectedRow();
            String maPB = tblPhongBan.getValueAt(viTri,4).toString();
            boolean kq = BLL.BLLPhongBan.KiemTraSua(SDT, tenPB, diaChi,maPB);
            if (kq) {
            BLL.BLLPhongBan.LayDuLieu(tblPhongBan);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

