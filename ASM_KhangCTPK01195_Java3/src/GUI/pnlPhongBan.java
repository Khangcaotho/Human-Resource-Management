    private void tblPhongBanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPhongBanMouseClicked
         if(tblPhongBan.getSelectedRowCount()>=1){
          btnxoa.setEnabled(true); 
        }
        if(tblPhongBan.getSelectedRowCount()>=1){
            btnSua.setEnabled(true);
            // lấy vị trí đc chọn
            int viTri = tblPhongBan.getSelectedRow();
            txtTenPB.setText(tblPhongBan.getValueAt(viTri,1).toString());
            txtdc.setText(tblPhongBan.getValueAt(viTri, 2).toString());
            txtsdt.setText(tblPhongBan.getValueAt(viTri, 3).toString());
        }else{ 
            btnSua.setEnabled(false);
        }
    }//GEN-LAST:event_tblPhongBanMouseClicked


